public class typecasting {
    public static void main(String args[]){
        // IN TYPE CASTING WE CONVERT A BIG DATA TYPE TO A SMALL DATA TYPE AS A 
        //RESULT WE MAY LOOSE A PART OF THE DATA 
        double a= 5311111.551;
        int b= (int)a;
        System.out.println(b);
    }
    
}
