public class numberpattern {
    public static void main(String[]args){
        int i = 8;
        int j = 1;
        for(int line=1; line<=i; line++){
            for(int symbol=1; symbol<=8; symbol++){
                System.out.print(j);
                j++;
            
            }
            System.out.println();
        }
    }
    
}
